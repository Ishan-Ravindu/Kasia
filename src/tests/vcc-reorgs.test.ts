describe("saars", () => {
  const saars = new SenderAndAcceptanceResolutionService(new RpcClient());

  it("should handle re-orgs", async () => {
    saars["onVirtualChainChanged"]({ data: vccEvent1.vcc });

    // has updated by accepting block
    expect(saars["transactionIdsByAcceptingBlock"]).key(
      vccEvent1.vcc.addedChainBlockHashes[0]
    );

    // has updated by txId
    expect(
      saars["acceptingBlockByTransactionId"][
        "9e0a5d5bf35b0de59904abe1c7719cabe71e25678f97e260ed3538793117eb47"
      ]
    ).toBe(vccEvent1.vcc.addedChainBlockHashes[0]);

    saars["onVirtualChainChanged"]({ data: vccEvent2.vcc });

    // has updated by accepting block
    expect(saars["transactionIdsByAcceptingBlock"]).key(
      vccEvent2.vcc.addedChainBlockHashes[0]
    );

    // has removed old accepting block
    expect(saars["transactionIdsByAcceptingBlock"]).not.key(
      vccEvent1.vcc.addedChainBlockHashes[0]
    );

    // has updated by txId
    expect(
      saars["acceptingBlockByTransactionId"][
        "9e0a5d5bf35b0de59904abe1c7719cabe71e25678f97e260ed3538793117eb47"
      ]
    ).toBe(vccEvent2.vcc.addedChainBlockHashes[0]);
  });
});
